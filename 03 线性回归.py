"""
第一节 一元线性回归

1.1 预测冰淇淋销量 
比方说温度越高 销量高 我们有温度x 销量y观测点 通过观测点就可以拟合数据了 
假设拟合一条直线 那么有了这条直线之后就可以进行预测了 由x得到y 预测出销量为y

1.2. 通过数学方法拟合
以上用观测点的方法是通过观察来拟合出一条直线 拟合的原则是所有点距离直线都最近
换句话来说就是误差最小 下面来用数学的方法严格定义一下误差;
'
在深度学习中 我们把收集到的真实数据叫做样本 样本构成数据集dataset
一条样本里的输入叫做特征feature 样本里希望模型预测的值叫 做标签label
深度学习模型本质上就是一个函数 函数的输入是feature 输出是预测值
训练过程就是调整函数内部的参数(parameter)/也可以叫权重weight 让预测值尽可能的接近label
当模型训练好后 就可以只输入新的样本的特征 模型根据内部学习到的参数 计算出预测值
'
'
在预测冰淇淋销量的例子里 我们可以假设拟合的线是 y = w*x 我们需要调整的参数就是w
我们给w一个初始化的值 比方说4 那么y = 4*x
通过这个初始化的值 我们可以得到不同x值对应的预测到的y 
将我们预测的y分别和原样本里的label进行比较 就得到了误差
把每个误差累加起来 通过调节参数w的值 能够让累加误差最小的w就是拟合我们数据最好的直线
补充 因为误差有正有负 为了防止直接相加正负抵消 所以取每个点的误差的平方再累加 作为最终的累加误差
'
在深度学习中 上述的平方累加误差(误差平方和)就被称为损失函数loss 损失函数衡量预测值和真实值之间的距离
模型训练的目的就是降低loss 得到loss为最小时候的参数w 通过这个w这个权重预测出y

1.3. 线性回归
还是回到一开始预测冰淇淋销量的例子 我们得到了loss 这个loss是有最小值的
在微积分里被叫做极值 我们知道 存在极小值的函数 在极小值处导数为0 所以我们让loss对w求导 并让导数=0 从而求解出w
这种针对一组点 找出最佳的拟合直线 就叫做线性回归
'
线性回归
线性回归就是假设标签label和特征feature之间存在线性关系 
线性回归模型训练的过程就是找到能让损失函数最小的 参数w的值的过程
'

1.4. 回归小tips
机器学习的算法分类有监督学习(有特征有标签) 无监督学习(有特征无标签 发现数据之间的模式) 强化学习(无特征无标签 只有奖励值R 通过优化参数得到更高的奖励)
监督学习又主要分为分类和回归
预测的是连续型变量则为回归
预测的是可数的离散型变量则是分类 根据特征对实例进行分类
回归这个名词与高尔顿有关 概念是统计学中的“回归到均值”
"""





"""
第二节 给线性回归加上偏置
1. 直线方程
y = w * x +b
通过不同的w和b可以生成不同的直线
b被称为偏置也可以叫截距
2. 加上偏置
上一届为了简化运算假设线性回归b=0 即直线肯定通过原点
这一届通过把b也设置为可训练的参数 增加对于预测的精度 将误差减小
线性函数 y = w*x +b
损失函数 loss = 有w和b的式子
以w为x b为y轴 loss为z轴绘制图像找最小值
根据偏导数的知识 即分别用loss对w和b求偏导 令偏导数=0 联立方程得到loss最小时候的w和b的取值
"""






"""
第三节 梯度下降算法
在深度学习中 对参数求最优解用的是梯度下降算法 
一方面 因为深度学习中需要优化的参数特别多 在大模型中有几百亿个参数 无法联立方程求解
另一方面  因为深度学习中引入了激活函数 激活函数增加了模型的非线性能力 解方程太复杂无法实现
1.一元函数求最小值
(1)步长:  
设置一个初始值w0
导数是否大于0确定w的移动方向
如何确定步长呢 步长可以取导数的值 由于移动方向要和导数方向相反 也就是给w0加上负导数值 不断更新w0使之不断接近全局最低点
(2)学习率:
实际中我们给步长还要乘一个系数 这是步长系数叫做学习率learning_rate (lr) 因为导数值作为步长并不总那么合适 所以需要设置学习率来精细调整
一般来说学习率小于1 比如0.001
(3)参数和超参数
参数是训练过程中由算法和模型调整的变量 是通过数据学习来的
超参数是模型训练前任未设置的变量 比如说学习率

2.多元函数求最小值
在实际情况中 我们都是要优化多个参数 比方说上一节的例子 我们的目标是找到loss最小时的w和b
与一元函数类似 先设置w和b的初始值w0和b0
你可以想象你是一个登山者，被困在一个云雾缭绕的大山里。你想尽快下山，但看不清远处的路。你会怎么选择眼前的路呢？梯度下降的做法是选择沿最陡峭的路向下走一段，然后再观察，再选择最陡峭的路向下
根据梯度定义我们知道沿着梯度方向函数值增长速度最快 负方向下降最快 所以沿着负方向给梯度值乘学习率lr作为步长
不断迭代得到loss最小的w和b

3.梯度下降算法的几个要素
(1)初始化:对于每个要优化的参数，随机初始化一个值。
(2)计算梯度:根据当前参数值，对每个参数求偏导，得到梯度值。
*** 梯度推导 ***
(3)迭代更新参数:让当前参数向量沿着梯度负方向，用学习率乘以梯度的值前进。然后再计算梯度。
(4)停止条件:可以设置迭代次数 当到达迭代次数后则停止更新。或者可以设置loss连续多个迭代不减小 则认为到达loss最小处 停止更新。

4.损失函数除以样本数
每个参数在每次梯度下降迭代时
都和损失函数的偏导数相关。损失函数不能和样本个数相关。之前我们定义的损失函数是所有样本的label和预测值的误差的平方和。
为了让训练稳定 不同的样本数也有差不多大小的偏导数值。一般在loss函数都会除以计算loss的样本数。
对于回归问题 一般采用的均方误差(Mean Squared Error,MSE)

5.不用担心鞍点和局部最优解
因为参数训练量真的很大。。
"""





"""
第四节 多元线性回归
4.1 添加更多特征
之前我们举的根据气温预测冰激凌销量的例子里 只有一个feature 那就是气温。但实际情况中，影响冰激凌销量的因素还有很多，比如价格。
现在我们就有了2个特征feature 一个偏置 一共3个参数需要优化。因为我们已经学了梯度下降算法 所以并不需要担心。下一节我们就动手实现梯度下降算法来找出最优的参数。
4.2 线性回归的一般表达式
多元线性回归的假设函数可以表示成两个向量的点积 其中模型参数w向量[w0,w1,..wn] 将偏置和特征系数进行融合
而特征向量x就是在第一个位置增加一个1 和代表偏置的参数对应上 特征向量 [1,x1,x2,..xn]
以4.1为例, 即 x=[1,x1,x2]=[1,气温,价格], w=[w0,w1,w2]=[偏置b, x1对应的权重w1,x2对应的权重w2]
最终可以表示为h_w(x) = w 点积 x
"""

"""
第五节 动手实现多元线性回归
"""
import torch
# Feature 数据
X = [[10, 3], [20, 3], [25, 3], [28, 2.5], [30, 2], [35, 2.5], [40, 2.5]]
# X = torch.tensor(X)
# print(X)
# print(X.shape)
y = [60, 85, 100, 120, 140, 145, 163]  # Label 数据
# 初始化参数
w = [0.0, 0.0, 0.0]  # w0, w1, w2
lr = 0.001  # 学习率
num_iterations = 1000  # 迭代次数
# 梯度下降
for i in range(num_iterations):
    # 预测值
    y_pred = [w[0] + w[1] * x[0] + w[2] * x[1] for x in X]
    # 计算损失
    loss = sum((y_pred[j] - y[j]) ** 2 for j in range(len(y))) / len(y)
    # 计算梯度
    grad_w0 = 2 * sum(y_pred[j] - y[j] for j in range(len(y))) / len(y)
    grad_w1 = 2 * sum((y_pred[j] - y[j]) * X[j][0] for j in range(len(y))) / len(y)
    grad_w2 = 2 * sum((y_pred[j] - y[j]) * X[j][1] for j in range(len(y))) / len(y)
    # 更新参数
    w[0] -= lr * grad_w0
    w[1] -= lr * grad_w1
    w[2] -= lr * grad_w2
    # if i < 3: # 只看前几次
    #     print(f"迭代 {i}:")
    #     print(f"  预测值样例: {y_pred[0]:.2f} (真实值: {y[0]})")
    #     print(f"  梯度: w0={grad_w0:.2f}, w1={grad_w1:.2f}, w2={grad_w2:.2f}")
    #     print(f"  更新后的权重: {w}")
    # 打印损失
    if i % 100 == 0:
        print(f"Iteration {i}: Loss = {loss}")
# 输出最终参数
print(f"Final parameters: w0 = {w[0]}, w1 = {w[1]}, w2 = {w[2]}")

